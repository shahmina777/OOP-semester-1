public class primitiveTypes{

  public static void main(String arg[]){
    
byte b = 1;
short s = 55;
int i = 10;
long l =100;


float f= 10.0f;
double d= 100.0;

boolean bool=true;
char c='s';


System.out.printf("value of byte: %\nd ",b);
System.out.printf("value of short: %\nd ",s);
System.out.printf("value of int : %\nd ",i);
System.out.printf("value of long: %\nd ",l);

System.out.printf("value of float: %\nf ",f);
System.out.printf("value of double: %\nf ",d);

System.out.printf("value of boolean: %\nb ",bool);
System.out.printf("value of double: %\nc ",c);



}
 }